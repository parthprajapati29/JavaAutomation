package coreJava;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		//increment
		for(int i=1;i<=5;i++) {
			System.out.println("Print value of i: "+i);
		}
		
		System.out.println();
		//decrement
		for (int i = 5; i >= 0; i--) {
			System.out.println("Print value of i: "+i);
		}
	}

}
